"""Export benchmark results to LaTeX tables and PNG charts."""

import json
import sys
from pathlib import Path

DIFFICULTY_ORDER = ["easy", "medium", "hard", "very-hard"]
TYPE_ORDER = ["qa", "fill_blank", "mcq", "true_false", "coding", "debug"]

# Difficulty labels used in the chart (skip very-hard to avoid crowding)
CHART_DIFFICULTIES = ["easy", "medium", "hard"]

TYPE_LABELS = {
    "qa": "QA",
    "fill_blank": "Fill Blank",
    "mcq": "MCQ",
    "true_false": "True/False",
    "coding": "Coding",
    "debug": "Debug",
}

DIFFICULTY_LABELS = {
    "easy": "Easy",
    "medium": "Medium",
    "hard": "Hard",
    "very-hard": "Very-Hard",
}


def _accuracy(correct: int, total: int) -> float:
    """Compute accuracy percentage."""
    return correct / total * 100 if total else 0.0


def _format_cell(correct: int, total: int) -> str:
    """Format a cell as '73.5% (324/441)' or '--' if no data."""
    if total == 0:
        return "--"
    pct = _accuracy(correct, total)
    return f"{pct:.1f}\\% ({correct}/{total})"


def _escape_latex(text: str) -> str:
    """Escape special LaTeX characters in model names."""
    return (
        text.replace("&", "\\&")
        .replace("%", "\\%")
        .replace("#", "\\#")
        .replace("_", "\\_")
    )


def load_result_files(paths: list[Path]) -> list[dict]:
    """Load and validate benchmark result JSON files.

    Args:
        paths: List of file paths to benchmark result JSON files.

    Returns:
        List of parsed result dicts, sorted by model name.

    Raises:
        ValueError: If a file is missing required keys.
    """
    results = []
    for p in paths:
        with open(p) as f:
            data = json.load(f)
        if "model" not in data or "stats" not in data:
            raise ValueError(
                f"Invalid result file (missing 'model' or 'stats'): {p}"
            )
        results.append(data)
    results.sort(key=lambda d: d["model"])
    return results


def find_result_files(
    output_dir: str,
    model_names: list[str] | None = None,
    latest: bool = False,
) -> list[Path]:
    """Find benchmark result JSON files in the output directory.

    Args:
        output_dir: Directory containing bench_*.json files.
        model_names: If provided, filter to files whose model matches.
        latest: If True, keep only the most recent file per model name.

    Returns:
        Sorted list of Path objects.
    """
    out = Path(output_dir)
    files = sorted(out.glob("bench_*.json"))
    if not files:
        return []

    if model_names is not None:
        filtered = []
        for f in files:
            data = json.loads(f.read_text())
            if data.get("model") in model_names:
                filtered.append(f)
        files = filtered

    if latest:
        by_model: dict[str, Path] = {}
        for f in files:
            data = json.loads(f.read_text())
            name = data["model"]
            if name not in by_model or f.stat().st_mtime > by_model[name].stat().st_mtime:
                by_model[name] = f
        files = list(by_model.values())

    return sorted(files)


def generate_latex_table(results: list[dict], output_dir: str = "./results") -> str:
    """Generate a LaTeX table from benchmark results.

    Produces two tables: accuracy by difficulty and accuracy by type.
    Uses the booktabs package for publication-quality formatting.

    Args:
        results: List of result dicts as loaded by load_result_files.
        output_dir: Directory to write the .tex file to.

    Returns:
        The LaTeX string.
    """
    lines = [
        "% Benchmark results — auto-generated by mlx-bench",
    ]

    # --- Table 1: Accuracy by Difficulty ---
    diff_cols = DIFFICULTY_ORDER
    diff_headers = [DIFFICULTY_LABELS[d] for d in diff_cols]

    lines.append(r"\begin{table}[htbp]")
    lines.append(r"\centering")
    lines.append(r"\caption{MLX Benchmark accuracy by difficulty}\label{tab:bench-difficulty}")
    col_spec = "l" + "r" * (1 + len(diff_cols))
    lines.append(r"\begin{tabular}{" + col_spec + "}")
    lines.append(r"\toprule")
    header = "Model & Overall & " + " & ".join(diff_headers) + r" \\"
    lines.append(header)
    lines.append(r"\midrule")

    for r in results:
        stats = r["stats"]
        model = _escape_latex(r["model"])
        overall = _format_cell(stats["correct"], stats["total"])
        row = f"\\texttt{{{model}}} & {overall}"
        for diff in diff_cols:
            bucket = stats.get("by_difficulty", {}).get(diff, {"total": 0, "correct": 0})
            row += " & " + _format_cell(bucket["correct"], bucket["total"])
        row += r" \\"
        lines.append(row)

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append(r"\end{table}")
    lines.append("")

    # --- Table 2: Accuracy by Type ---
    type_cols = TYPE_ORDER
    type_headers = [TYPE_LABELS[t] for t in type_cols]

    lines.append(r"\begin{table}[htbp]")
    lines.append(r"\centering")
    lines.append(r"\caption{MLX Benchmark accuracy by type}\label{tab:bench-type}")
    col_spec = "l" + "r" * len(type_cols)
    lines.append(r"\begin{tabular}{" + col_spec + "}")
    lines.append(r"\toprule")
    header = "Model & " + " & ".join(type_headers) + r" \\"
    lines.append(header)
    lines.append(r"\midrule")

    for r in results:
        stats = r["stats"]
        model = _escape_latex(r["model"])
        row = f"\\texttt{{{model}}}"
        for t in type_cols:
            bucket = stats.get("by_type", {}).get(t, {"total": 0, "correct": 0})
            row += " & " + _format_cell(bucket["correct"], bucket["total"])
        row += r" \\"
        lines.append(row)

    lines.append(r"\bottomrule")
    lines.append(r"\end{tabular}")
    lines.append(r"\end{table}")

    latex = "\n".join(lines) + "\n"

    # Write to file
    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    (out / "bench_results.tex").write_text(latex)

    return latex


def generate_plot(results: list[dict], output_dir: str = "./results") -> None:
    """Generate a PNG bar chart from benchmark results.

    Produces a grouped bar chart showing accuracy per model,
    broken down by Overall, Easy, Medium, Hard.

    Args:
        results: List of result dicts as loaded by load_result_files.
        output_dir: Directory to write the .png file to.

    Raises:
        SystemExit: If matplotlib is not installed.
    """
    try:
        import matplotlib
        matplotlib.use("Agg")
        import matplotlib.pyplot as plt
    except ImportError:
        sys.exit(
            "matplotlib is required for --plot. Install it with:\n"
            "  pip install matplotlib\n"
            "  or: pip install mlx-benchmark[plot]"
        )

    if not results:
        print("No results to plot.")
        return

    models = [r["model"] for r in results]
    bar_labels = ["Overall"] + [DIFFICULTY_LABELS[d] for d in CHART_DIFFICULTIES]

    # Collect data: each row is [overall, easy, medium, hard]
    data = []
    for r in results:
        stats = r["stats"]
        overall_pct = _accuracy(stats["correct"], stats["total"])
        row = [overall_pct]
        for d in CHART_DIFFICULTIES:
            bucket = stats.get("by_difficulty", {}).get(d, {"total": 0, "correct": 0})
            row.append(_accuracy(bucket["correct"], bucket["total"]))
        data.append(row)

    # Plot
    n_models = len(models)
    n_bars = len(bar_labels)
    x = list(range(n_models))
    width = 0.8 / n_bars
    colors = ["#4C72B0", "#55A868", "#C44E52", "#8172B3"]

    fig, ax = plt.subplots(figsize=(max(8, n_models * 2.5), 6))

    for i, label in enumerate(bar_labels):
        offsets = [xi + i * width - 0.4 + width / 2 for xi in x]
        heights = [row[i] for row in data]
        ax.bar(offsets, heights, width=width, label=label, color=colors[i % len(colors)])

    # Overall average line
    overall_avg = sum(row[0] for row in data) / n_models if n_models else 0
    ax.axhline(y=overall_avg, color="gray", linestyle="--", linewidth=1, label=f"Avg ({overall_avg:.1f}%)")

    ax.set_xlabel("Model")
    ax.set_ylabel("Accuracy (%)")
    ax.set_title("MLX Benchmark Results")
    ax.set_xticks(x)
    ax.set_xticklabels(models, rotation=30, ha="right")
    ax.set_ylim(0, 105)
    ax.legend(loc="upper right", bbox_to_anchor=(1.0, 1.0))
    ax.grid(axis="y", alpha=0.3)

    fig.tight_layout()

    out = Path(output_dir)
    out.mkdir(parents=True, exist_ok=True)
    output_path = out / "bench_results.png"
    fig.savefig(output_path, dpi=150)
    plt.close(fig)

    print(f"  Chart saved to {output_path}")